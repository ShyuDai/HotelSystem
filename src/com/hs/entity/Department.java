package com.hs.entity;

public class Department {//部门
	private int depId;//序号
	private String depNo;//部门号
    private String depName;//部门名称
    private String depManager;//部门经理
    private int   empCount;//员工数量
    
	public int getDepId() {
		return depId;
	}
	public void setDepId(int depId) {
		this.depId = depId;
	}
	public String getDepNo() {
		return depNo;
	}
	public void setDepNo(String depNo) {
		this.depNo = depNo;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public String getDepManager() {
		return depManager;
	}
	public void setDepManager(String depManager) {
		this.depManager = depManager;
	}
	public int getEmpCount() {
		return empCount;
	}
	public void setEmpCount(int empCount) {
		this.empCount = empCount;
	}

	public String toString() {
		return "Department [depId=" + depId + ", depNo=" + depNo + ", depName=" + depName + ", depManager=" + depManager
				+ ", empCount=" + empCount + "]";
	}
	
	
	

}
